package com.example.appquiz.Model;

public class DapAn {
    int id;
    String dapAn;

    public DapAn(int id, String dapAn) {
        this.id = id;
        this.dapAn = dapAn;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDapAn() {
        return dapAn;
    }

    public void setDapAn(String dapAn) {
        this.dapAn = dapAn;
    }
}
